max_height = 0  # ความสูงสูงสุดที่มองเห็นจนถึงตอนนี้
count = 0       # จำนวนตึกที่มองเห็น

while True:
    h = int(input())  # รับความสูงตึก
    if h == -1:       # เจอ -1 หยุดรับข้อมูล
        break
    if h > max_height:  # ถ้าตึกสูงกว่าตึกสูงสุดที่ผ่านมา
        count += 1      # มองเห็นเพิ่ม 1
        max_height = h  # อัปเดตความสูงสูงสุด

print(count)