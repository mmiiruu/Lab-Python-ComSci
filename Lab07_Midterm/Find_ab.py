# รับค่าจำนวนเต็มบวก n
n = int(input())

# กำหนดค่าเริ่มต้นของผลบวกเป็นค่าที่มาก ๆ
min_sum = n + 1

# วนหาตัวประกอบ a ตั้งแต่ 1 ถึง n
a = 1
while a <= n:
    if n % a == 0:   # ถ้า a หาร n ลงตัว
        b = n // a   # b คืออีกตัวประกอบ
        s = a + b    # ผลบวกของคู่ตัวประกอบ
        if s < min_sum:
            min_sum = s
    a += 1

# แสดงผลลัพธ์
print(min_sum)
