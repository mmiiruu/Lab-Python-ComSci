# 1. รับเลขจำนวนเต็มบวกจากผู้ใช้
n = int(input())

# 2. เตรียมตัวแปรเก็บผลคูณของเลขคู่
product = 1

# 3. ตัวแปรตรวจสอบว่ามีเลขคู่หรือไม่
found = False

# 4. วนดูเลขทุกหลักของ n
temp = n
while temp > 0:
    digit = temp % 10           # หาหลักสุดท้าย
    if digit % 2 == 0:          # ตรวจสอบว่าเลขคู่หรือไม่
        product *= digit        # คูณเลขคู่กับผลคูณสะสม
        found = True            # ตั้ง found = True เพราะเจอเลขคู่
    temp //= 10                 # ตัดเลขหลักสุดท้ายทิ้ง

# 5. แสดงผลลัพธ์
if found:
    print(product)              # ถ้าเจอเลขคู่ → แสดงผลคูณ
else:
    print(-1)                   # ถ้าไม่เจอเลขคู่ → แสดง -1
